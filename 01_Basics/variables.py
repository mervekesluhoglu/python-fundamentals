# 1. Tamsayı (Integer) Değişkenler
tamsayi_1 = 10
tamsayi_2 = 15
print(f"Birinci sayı: {tamsayi_1}, İkinci sayı: {tamsayi_2}")

# 2. Kesirli Sayı (Float) Değişkenler
kesirli_sayi = 10.5
print(f"Kesirli sayı örneği: {kesirli_sayi}")

# 3. Metin (String) İşlemleri
metin1 = "Merhaba"
metin2 = "Dünya"
mesaj = metin1 + " " + metin2
print(f"Mesaj: {mesaj}")

# 4. Python Syntax (Büyük/Küçük Harf Duyarlılığı)
temel = 6
TEMEL = 7
print(f"Küçük harfli temel: {temel}")
print(f"Büyük harfli TEMEL: {TEMEL}")
